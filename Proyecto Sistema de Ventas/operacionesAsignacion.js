// el primer operador de asignacion que debemos de aprender es = 
let a = 4

// Luego debemos de cononcer +=, operador de asignación compuesto
a += 3 // esto es igual a-> a = a +3
console.log(a)

// También se puede hacer para la resta
a -= 2
console.log(a) 

// Para la multiplicación

a *= 2
console.log(a)

// Para la división
a /= 2
console.log(a)

// Para el módulo

a %= 2
console.log(a)